# if statement = a block of code that will execute only when it is true
age = int(input("how old are you? :"))
if age == 100:
    print("you have been lived  your life to fullest")
elif age >= 18:
    print("you are an adult")
elif age < 0:
    print("you havent been born yet")
else:
    print("you are an child")

